#include "libft.h"

int	ft_onlydigits(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 48 && str[i] <= 57) || str[i] == '-' || str[i] == '+')
			i++;
		else
			return (0);
	}
	return (1);
}
